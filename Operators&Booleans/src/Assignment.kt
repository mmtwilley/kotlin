fun main(args: Array<String>){
//    Equals or Assignment
    val dozen = 12;
    var months = dozen;
    println(months)

//    Reference
    val username = "Michael"
    val greeting = "Hello,$username"
    println(greeting)

//Reference Expression
    val products = 3
    val price = 9.99
    println("Your total is $${products * price}")
}